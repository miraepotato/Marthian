    /**
     * 출동부대(센터/구조대/지역대/출동대 등) 좌표 조회
     * - 1순위: "소방서::부대명" 정밀 좌표
     * - 2순위: 부대명 단독 key(과거 호환용; 유일한 경우에만 기대)
     * - 3순위: 소속 소방서 본서(HQ)
     */
    fun getCenterLatLng(centerName: String, stationName: String): LatLng {
        val precise = centerDetailCoords[makeCenterKey(stationName, centerName)]
        if (precise != null) return precise

        val legacy = centerDetailCoords[centerName]
        if (legacy != null) return legacy

        return getStationLatLng(stationName)
    }

